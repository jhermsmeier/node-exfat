/**
 * FSError
 * @return {FSError}
 */
function FSError( code, msg ) {
  msg = FSError.ERROR_CODE[ code ] + ( msg ? ': ' + msg : '' )
  var error = new Error( msg )
  error.code = code
  return error
}

